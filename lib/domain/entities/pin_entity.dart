class PinEntity {
  final int id;
  final String imageUrl;
  final String detailUrl;
  final String title;
  final String author;
  final String avgColor;
  final int width;
  final int height;

  PinEntity({
    required this.id,
    required this.imageUrl,
    required this.detailUrl,
    required this.title,
    required this.author,
    required this.avgColor,
    required this.width,
    required this.height,
  });
}
